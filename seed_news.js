async function run() {
  let count = 0;
  for (const n of news) {
    await pool.execute(
      `INSERT INTO news (author_id, event_id, title, slug, excerpt, content, cover_image, category, status, is_featured, published_at)
       VALUES (1, ?, ?, ?, ?, ?, ?, ?, 'published', ?, ?)`,
      [n.event_id, n.title, n.slug, n.excerpt, n.content, n.cover_image, n.category, n.is_featured, n.published_at]
    );
    count++;
    process.stdout.write('✓ [' + n.category + '] ' + n.title.slice(0, 55) + '\n');
  }
  const [[{ total }]] = await pool.execute('SELECT COUNT(*) as total FROM news');
  process.stdout.write('\nДодано: ' + count + '  |  Всього у БД: ' + total + '\n');
  pool.end();
}

run().catch(e => { console.error(e.message); process.exit(1); });
